import { Schema } from 'mongoose';

// Definición del esquema de usuario (UserSchema)
export const UserSchema = new Schema({

    // Definición de cada campo del esquema
    name: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    age: {
        type: Number,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});